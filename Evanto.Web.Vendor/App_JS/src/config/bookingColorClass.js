export default (type) => {
    switch (type) {
        case 0:
            return 'avatar_color_pink'
        case 1:
            return 'avatar_color_amber' //amber
        case 2:
            return 'avatar_color_green' //green
        case 3:
            return 'avatar_color_red' //red
        case 4:
            return 'avatar_color_blue' //blue
    }
}